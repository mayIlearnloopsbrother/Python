#!/usr/bin/python3

#THIS PROGRAM USES SORT, REVERSE AND SORTED 

places = ['uk', 'india', 'nepal', 'china']
print("\n" + str(places))
print("\n" + str(sorted(places)))
print("\n" + str(places))
print("\n" + str(sorted(places, reverse=True)))
print("\n" + str(places))
places.reverse()
print("\n" + str(places))
places.reverse()
print("\n" + str(places))
places.sort()
print("\n" + str(places))
places.sort(reverse=True)
print("\n" + str(places))
